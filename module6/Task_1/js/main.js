let mainBlock, block;

inputApply.addEventListener("click", ()=> {
    if (mainBlock != undefined) {
        mainBlock.remove()
    }
    const tableSize = inputSize.value
    mainBlock = document.createElement('div')
    mainBlock.className = "main-block"
    mainBlock.style.width = 100 * tableSize + "px";
    mainBlock.style.height = 100 * tableSize + "px";
    document.body.append(mainBlock)

    
    for (let i = 0; i < tableSize * tableSize; i++) {
        block = document.createElement('div')
        block.className = "block"
        if (i % tableSize == tableSize - 1 && i != tableSize * tableSize - 1) {
            block.className += " bottom-borders";
        }
        else if (i >= tableSize * tableSize - tableSize && i != tableSize * tableSize - 1) {
            block.className += " right-borders"
        }
        else if (i != tableSize * tableSize - 1) {
            block.className += " bottom-borders";
            block.className += " right-borders"
        }
        mainBlock.append(block)
    }

    for (let i = 0; i < tableSize * tableSize; i++) {
        for (let j = 0; j < tableSize * tableSize; j++) {
            if (matrix[i][j] && i == j - 1) {
                mainBlock.childNodes[i].classList.remove("right-borders")
            }
            if (matrix[i][j] && i == j - tableSize) {
                mainBlock.childNodes[i].classList.remove("bottom-borders")
            }
        }
    }  
})


